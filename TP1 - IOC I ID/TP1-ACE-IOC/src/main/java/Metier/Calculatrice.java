package Metier;

public class Calculatrice {
        public double somme(double a, double b){
            return a+b;}
        public double mult(double a, double b){
            return a*b;
    }
    public double substract(double a, double b){
        return a-b;
    }
    public  double divison(double a, double b){
        return a/b;
    }
}
